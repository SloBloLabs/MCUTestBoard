/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_ADC1_Init();
  MX_USB_DEVICE_Init();
  /* USER CODE BEGIN 2 */

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */

  // Follow https://youtu.be/kDaQO_x8auk?si=l_U3NRmKQmfKEMqJ for USB CDC setup

  LL_ADC_Enable(ADC1);
  uint32_t usbTxTime = 0;

  while (1)
  {
	  uint32_t timeNowMS = HAL_GetTick();

	  LL_ADC_INJ_StartConversionSWStart(ADC1);
	  uint16_t adcValue = LL_ADC_INJ_ReadConversionData12(ADC1, LL_ADC_INJ_RANK_1);

	  // 0 <= adcValue <= 4095 -> 16 leds
	  double prod = adcValue * 15.0;
	  uint8_t led = (int)(prod / 4095 + 0.5);

	  // Reset GPIOB
	  GPIOB->BSRR = 0xffff << 16;
      // Set pin according to led
	  GPIOB->BSRR = (1 << led);

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
	  if(timeNowMS - usbTxTime > 500) {
		  usbTxTime = timeNowMS;

		  usbTxBufLen = snprintf((char *) usbTxBuf, USBBUF_MAXLEN, "[%02lu:%02lu] %u\r\n", (timeNowMS / 60000), (timeNowMS / 1000) % 60, adcValue);

		  CDC_Transmit_FS(usbTxBuf, usbTxBufLen);

	  }

	  if (usbRxFlag && usbRxBufLen) {

		  // Echo incoming data
		  CDC_Transmit_FS(usbRxBuf, usbRxBufLen);

		  for(uint8_t i = 0; i < usbRxBufLen; ++i) {

			  // Reset GPIOB
			  GPIOB->BSRR = 0xffff << 16;
		      // Set all pins
			  GPIOB->BSRR = 0xffff;

			  LL_mDelay(100);

			  // Reset GPIOB
			  GPIOB->BSRR = 0xffff << 16;

			  LL_mDelay(100);
		  }

		  usbRxFlag = 0;
		  usbRxBufLen = 0;

	  }
  }
  /* USER CODE END 3 */
}

/**
  * @brief System Clock Configuration
  * @retval None
  */
